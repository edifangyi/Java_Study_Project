package com.imooc;

public class Car {
	int model;		//型号
	String name;	//名称
	int people;		//载人数
	double volume;	//载货量
	double money;	//租金
	
	public int getModel() {
		return model;
	}
	public String getName() {
		return name;
	}
	public int getPeople() {
		return people;
	}
	public double getVolume() {
		return volume;
	}
	public double getMoney() {
		return money;
	}
	
}
